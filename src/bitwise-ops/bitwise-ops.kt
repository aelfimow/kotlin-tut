fun main(args: Array<String>) {
    println("result: ${1 shl 1}")
    println("result: ${2 shr 1}")
    println("result: ${2 ushr 1}")
    println("result: ${-1 shl 1}")
    println("result: ${-2 shr 1}")
    println("result: ${1 and 3}")
    println("result: ${1 or 3}")
    println("result: ${1 xor 3}")
    println("result: ${1.inv()}")
}
